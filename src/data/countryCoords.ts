export function lookupCountry(name: string): CountryCoord | null {
  // exact match
  const exact = COUNTRY_COORDS[name.trim()];
  if (exact) return exact;
  // case-insensitive match
  const lower = name.trim().toLowerCase();
  const key = Object.keys(COUNTRY_COORDS).find((k) => k.toLowerCase() === lower);
  return key ? COUNTRY_COORDS[key] : null;
}
