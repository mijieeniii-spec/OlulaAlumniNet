function generateTeachers(names: string[], year: 2024 | 2025 | 2026): Teacher[] {
  return names.map((name, i) => ({
    id: year * 100 + i + 1,
    name,
    email: `${name.split(".")[1]?.trim().toLowerCase().replace(/\s/g, "") || "teacher"}@teacher.olula.mn`,
    photo: `https://api.dicebear.com/7.x/personas/svg?seed=teacher${year}${i}`,
    subject: subjects[i % subjects.length],
    year,
    role: i === 0 ? "Анги даасан багш" : "Багш",
  }));
}

